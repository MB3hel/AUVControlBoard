#include <framework.h>
#include <FreeRTOS.h>
#include <task.h>
#include <led.h>
#include <delay.h>
#include <math.h>
#include <usb.h>
#include <thruster.h>
#include <threads.h>


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Program Entry point / startup
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main(void){
    // -------------------------------------------------------------------------
    // System & Peripheral Initialization
    // -------------------------------------------------------------------------
    init_frameworks();
    delay_init();
    led_init();
    usb_init();
    thruster_init();
    // -------------------------------------------------------------------------
    
    // -------------------------------------------------------------------------
    // RTOS task initialization
    // -------------------------------------------------------------------------
    threads_init();
    // -------------------------------------------------------------------------

    // -------------------------------------------------------------------------
    // RTOS Startup
    // -------------------------------------------------------------------------
    vTaskStartScheduler();
    // -------------------------------------------------------------------------

    // Start scheduler should never return. This should never run, but is
    // included to make debugging easier in case it does
    while(1){
        asm("nop");
    }
}

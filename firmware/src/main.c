#include <atmel_start.h>
#include <pccomm.h>


int main(void){
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// Initialization
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    
    atmel_start_init();                             // Initialize ASF4 drivers & middleware
    pccomm_init();                                  // Initialize USB communications with PC

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /// Main loop
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    while (1) {
        pccomm_write_msg((uint8_t[]){'H', 'e', 'l', 'l', 'o'}, 5);
        delay_ms(1000);
    }
}
